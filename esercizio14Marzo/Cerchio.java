package esercizio14Marzo;

public class Cerchio extends Shape{
    private double raggio;

    double getRaggio(){
        return this.raggio;
    }

    double getArea(){
        double Area = this.raggio;
        return Area;
    }

    double getPerimetro(){
        double Perimetro = this.raggio * 2;
        return Perimetro;
    }

}
